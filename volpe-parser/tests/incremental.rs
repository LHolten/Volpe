mod common;
use common::*;

#[test]
fn simple() {
    test_edits!(
        ["a", (0, 0), (0, 0)]
        [" ", (0, 1), (0, 0)]
        ["+", (0, 2), (0, 0)]
        [" ", (0, 3), (0, 0)]
        ["b", (0, 3), (0, 0)]
        ["(", (0, 0), (0, 0)]
        [" ", (0, 2), (0, 0)]
        [")", (0, 4), (0, 0)]
    );
}

#[test]
fn found_by_fuzz() {
    test_edits!(
        ["A.AA", (0, 0), (0, 0)]
        [" .A ", (0, 0), (0, 0)]
        ["+", (0, 0), (0, 0)]
        [" A", (0, 0), (0, 0)]
        ["A&", (0, 0), (0, 0)]
        [".,", (0, 2), (0, 0)]
        ["A", (0, 14), (0, 0)]
    );
}
